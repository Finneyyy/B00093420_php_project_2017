	<h1>Plot</h1>
	
	<h2>1957-1958</h2>
	<p>
		During heavy rain in Derry, Main, six year old George 'Georgie' Denbrough follows his paper boat along a gutter. To Georgie's disappointment, the boat is washed into a storm drain. As he looked into the drain, he sees a pair of yellow eyes, which seem to be glowing. Startled, Georgie looks to see a man dressed in a silver clown suit, who introduces himself as 'Mr. Bob Gray', a.k.a "Pennywise the Dancing Clown". Pennywise offers Georgie a balloon, which Georgie refuses; however, the clown tempts Georgie to reach into the drain to retrieve his boat. Pennywise then severs the boy's arm, leaving Georgie to either bleed out, or to die of shock. Moments later, Dave Gardner, the neighbour, gets to Georgie, but he is already dead.
	</p>
	
	<p>
		The following June, Benjamin 'Ben' Hanscom, an overweight eleven year old boy, is bullied and harassed by a gang of bullies led by Henry Bowers, who threatened to carve his name into Ben's stomach with his switchblade. On the last day of school, Ben hides from the gang in a place called the Barrens, where he befriends two people, Edward 'Eddie' Kaspbrak, and William 'Bill' Denbrough. The three boys later befriend Richard 'Richie' Tozier, Stanley 'Stan' Uris, Beverly 'Bev' Marsh, and Michael 'Mike' Hanlon. They eventually refer their group as "The Losers Club".
	</p>
	
	<p>
		As the summer draws on, the Losers realise that each of them have had an encounter with an alien shape-shifting entity that takes the form of whatever they fear most. Due to the unknown origin of the monster, the Losers refer to the creature as 'It' and link It with a series of recent child murders, including that of Eddie Corcoran, who is killed by the monster in the form of the Gill-man.
	</p>
	
	<p>
		Meanwhile, a further unhinged and sadistic Henry Bowers begins focusing his attention on his black neighbour, Mike Hanlon and his father, including killing the boy's dog. During early July, Henry and his gang chase the terrified Mike into the Barrens, where he meets and is rescued by the Losers, who beat the bullies in a violent rock fight. Henry, who is left humiliated and defeated, swears revenge. Mike then joins the now-complete Losers' Club. He tells the Losers that he was attacked by It in the form of a flesh-eating bird, and shows them his father's scrapbook, which reveals that It has been around for hundreds of years. The Losers then begin to suspect that It has complete control over Derry, due to the number of unsolved disappearences, and the violent tragedies that are either go unnoticed or are forgotten by the adults in the town.
	</p>
	
	<p>
		After more encounters with It, the Losers create a makeshift Native American smokehole, which Richie and Mike use to hallucinate It's origins. After doing so, the discover that It came to Derry millions of years before in an asteroid-like impact, and that every 27 years It awakens from sleep under the town's sewers, usually after a tragedy or terrible event, to feed on children for around 12-16 months.
	</p>
	
	<p>
		In late July, Eddie is hospitalised after an attack by Henry and several of his friends. Spying on them, Bev witnesses one of the bullies, Patrick Hockstetter, trying to empty a fridge which he had been using to trap and kill injured animals, only to be killed by It in the form of blood-sucking insects. Later on, the Losers discover a message from It written in Patrick's blood, warning them that It will kill them.
	</p>
	
